l = float(input("Введите длину прямоугольного параллелепипеда: "))
w = float(input("Введите ширину прямоугольного параллелепипеда: "))
h = float(input("Введите высоту прямоугольного параллелепипеда: "))
s= 2 * (l * w + l * h + w * h)
v = l * w * h
print("Площадь поверхности равна", s)
print("Объем равен", v)
